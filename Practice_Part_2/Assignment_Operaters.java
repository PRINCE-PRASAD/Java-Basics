/**
 * AssignmentOperaters
 */
public class Assignment_Operaters {

    public static void main(String[] args) {
        int num1 = 8;
        int num2 = 5;

        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1 % num2);
        //it will give remain of division
        System.out.println(num1/num2);
        // it give Quosent of division
        System.out.println(num1+5);
        System.out.println(num1+=5);
        num1--;
        System.out.println(num1);
        num1++;   // post increment 
        ++num1;   // pre increment
        System.out.println(num1);
        // System.out.println(num1--);
    }
}